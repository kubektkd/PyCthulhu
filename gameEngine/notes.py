def show_owned_notes():
    global choice, notes, ownedNotes
    while choice != 0:
        print("╒═════════════════╕")
        print("│ WYBIERZ NOTATKĘ │")
        print("╘═════════════════╛")
        for note in ownedNotes:
            print(f"{note}) {notes[note - 1].get('name')}")
        print("0) Powrót")
        choice = inputNumber("Wybór > ")
        clear()
        show_note(choice)
    choice = 99


